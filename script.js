document.addEventListener("DOMContentLoaded", function () {
    const menuContainer = document.querySelector('.menu-container');
    const rate1Checkbox = document.getElementById('rate1');
    const rate2Checkbox = document.getElementById('rate2');

    function showRecipes(filterType, ratingFilter, dishSearch) {
        menuContainer.innerHTML = '';

        dishes.forEach(dish => {
            if ((!filterType || dish.type === filterType) && (!ratingFilter || (ratingFilter === '4+' && dish.rating >= 4) || (ratingFilter === 'below4' && dish.rating < 4))) {
                const menuItem = document.createElement('div');
                menuItem.innerHTML = `
                <img src="${dish.imageSrc}" alt="${dish.name}">
                <div class="dish-info"> <!-- Added a class for styling -->
                    <h5>${dish.type}</h5>
                    <span class="menu-details">
                        <h2>${dish.name}</h2>
                        <p>${dish.rating} <i class="fa-solid fa-star"></i></p>
                    </span>
                    <span>
                        <h4 class="dishtime">${dish.time}</h4>
                        <span  class="likemsg">
                        <i class="far fa-heart ${dish.isLiked ? 'liked' : ''}" data-dish="${dish.name}"></i>
                        <i class="far fa-comment"></i>
                        </span>
                    </span>
                </div>
                `;

                menuContainer.appendChild(menuItem);




                const heartIcon = menuItem.querySelector('.fa-heart');
                heartIcon.addEventListener('click', function () {
                    toggleLike(dish.name);
                });

                const spanElements = menuItem.querySelectorAll('span');
                spanElements.forEach(span => {
                    span.style.cssText = "display: flex; align-items: center; justify-content: space-between;";
                });
            }
        });
    }

    function toggleLike(dishName) {
        const dish = dishes.find(item => item.name === dishName);
        if (dish) {
            dish.isLiked = !dish.isLiked;
            const heartIcon = document.querySelector(`.fa-heart[data-dish="${dish.name}"]`);
            heartIcon.classList.toggle('liked', dish.isLiked);
        }
        
    }



    function showRecipesByRating() {
        const ratingFilter = rate1Checkbox.checked ? '4+' : (rate2Checkbox.checked ? 'below4' : null);

        showRecipes(null, ratingFilter);
    }

    showRecipes();

    rate1Checkbox.addEventListener('change', showRecipesByRating);
    rate2Checkbox.addEventListener('change', showRecipesByRating);

    const allRecipesBtn = document.querySelector('#allRecipesBtn');
    allRecipesBtn.addEventListener('click', function () {
        showRecipes();
    });

    const vegRecipesBtn = document.querySelector('#vegRecipesBtn');
    vegRecipesBtn.addEventListener('click', function () {
        showRecipes('veg');
    });

    const nonVegRecipesBtn = document.querySelector('#nonVegRecipesBtn');
    nonVegRecipesBtn.addEventListener('click', function () {
        showRecipes('non-veg');
    });
});

document.addEventListener("DOMContentLoaded", function () {
    const homeIcon = document.getElementById('home');
    const navList = document.querySelector('.nav-list');

    homeIcon.addEventListener('click', function () {
        navList.style.display = (navList.style.display === 'block' || navList.style.display === '') ? 'block' : 'none';
        navList.style.flexDirection = "column";
    });
});
